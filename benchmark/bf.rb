def compile(prog)
  bytes = []
  n = 0
  i = 0
  proglen = prog.size
  proglen.times do |cnt|
    ch = prog[cnt]
    sign = 2
    case ch
    when '>', '<'
      if ch == '<' then
        sign = 0
      end
      n += 1

      if ch != prog[i + 1] then
        bytes << ((n << 4) | 0b000 | sign)
        n = 0
      end

    when '+', '-'
      if ch == '-' then
        sign = 0
      end
      n += 1

      if ch != prog[i + 1] then
        bytes <<  ((n << 4) | 0b001 | sign)
        n = 0
      end

    when '.'
      bytes <<  0b100

    when ','
      bytes <<  0b110

    when '['
      nest = 0
      j = i + 1
      off = 1
      while prog[j] != ']' or nest != 0 do
        case prog[j]
        when '['
          nest += 1
          off += 1

        when ']'
          nest -= 1
          off += 1

        when '<',  '>',  '+', '-'
          prevch = prog[j]
          while prevch == prog[j + 1] do
            j += 1
          end
          off += 1

        when '.', ','
          off += 1
        end

        j += 1
      end
      bytes << ((off << 4) | 0b111)

    when ']'
      nest = 0
      j = i - 1
      off = 1
      while prog[j] != '[' or nest != 0 do
        case prog[j]
        when ']'
          nest += 1
          off += 1

        when '['
          nest -= 1
          off += 1

        when '<',  '>',  '+', '-'
          prevch = prog[j]
          while prevch == prog[j - 1] do
            j -= 1
          end
          off += 1

        when '.', ','
          off += 1
        end
        j -= 1
      end
      bytes << ((off << 4) | 0b101)

    end
    i += 1
  end

  optimize(bytes)
end

def optimize(bytes)
  bytes.each_with_index do |n, i|
    if n & 7 == 7 then
      # [ instruction
      nextpos = n >> 4
      if nextpos == 2 and bytes[i + 1] - (1 << 4) == 0b001 then
        # [ - ] Zero clear
        bytes[i] = (bytes[i] & (~15)) | 0b1000
      elsif nextpos == 5 and bytes[i + 1] - (1 << 4) == 0b001 and
          bytes[i + 2] & 0b101 == 0b000 and bytes[i + 3] & 0b101 == 0b001 and
          bytes[i + 4] & 0b101 == 0b000 then
        # [->>+<<] or [->>-<<] Add or Sub multiple
        bytes[i] = (bytes[i] & ~((1 << 4) - 1)) | 0b1001 | (bytes[i + 3] & 0b010)
      elsif nextpos == 2 and bytes[i + 1] & 0b101  == 0b001 then
        # [>>>...] or [<<<...] search 0 memptr
        bytes[i] = (bytes[i] & ~((1 << 4) - 1)) | 0b1100 | (bytes[i + 1] & 0b010)
      end
    end
  end
  bytes
end

def eval(bytes)
  mem = []
  1024.times do |i|
    mem[i] = 0
  end
  pc = 0
  ptr = 0

  while inst = bytes[pc] do
    pc = pc + 1
    if (inst & 8) == 0 then
      # Basic instruction
      if (inst & 4) == 0 then
        # < > + -
        if (inst & 1) == 0 then
          # < >
          ptr += (inst >> 4) * ((inst & 2) - 1)
        else
          # + -
          mem[ptr] += (inst >> 4) * ((inst & 2) - 1)
        end
      else
        # . , [ ]
        if (inst & 1) == 0 then
          # . ,
          if inst & 2 == 0 then
            print(mem[inst].chr)
          else
            #mem[inst] = getc
          end

        else
          # [ ]
          if (inst & 2) == 2 then
            if  mem[ptr] == 0 then
              pc += (inst >> 4)
            end
          else
            if mem[ptr] != 0 then
              pc -= (inst >> 4)
            end
          end
        end
      end
    else

      # Extend instruction
      if (inst & 4) == 0 then
        if (inst & 1) == 0 then
          # memory clear
          mem[ptr] = 0
        else
          # add/sub memory
          offset = bytes[pc + 1] >> 4
          sign = (bytes[pc + 1] & 2) - 1
          mem[ptr + offset * sign] += mem[ptr] * ((inst & 2) - 1)
          mem[ptr] = 0
        end

      else
        if (inst & 1) == 0 then
          # search 0 cell
          step = bytes[pc + 1] >> 4
          sign = (bytes[pc + 1] & 2) - 1
          while mem[ptr] != 0 do
            ptr += step * sign
          end
        end
      end
      pc += (inst >> 4)
    end
  end
end


